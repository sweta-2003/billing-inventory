    private void itemAddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemAddButtonActionPerformed
        // TODO add your handling code here:
        String name = itemName.getText();
        String quantity = itemQuantity.getText();
        String cost = costField.getText();
        
        Integer value=Integer.parseInt(cost)*Integer.parseInt(quantity);
        nList.add(name);
        qList.add(" x "+quantity);
        rList.add(value.toString() );
        DefaultListModel<String> m = new DefaultListModel<>();
        DefaultListModel<String> m2 = new DefaultListModel<>();
        DefaultListModel<String> m3 = new DefaultListModel<>();
        nameList.setModel(m);
        quantityList.setModel(m2);
        costList.setModel(m3);
        m.addAll(nList);
        m2.addAll(qList);
        m3.addAll(rList);
//        reduceInventory(name,quantity);
        totalPrice.setText("Rs. "+calcTotal(rList));
       
    }//GEN-LAST:event_itemAddButtonActionPerformed

