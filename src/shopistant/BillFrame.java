    private String getProductName(String id) {
        try {
            String result = "";
            String query = String.format("Select name from products where id=\"%s\"",id);
            c1 = connectDb.liteConnect();
            s = c1.createStatement();
            r = s.executeQuery(query);
            if(r.isBeforeFirst()){
            while(r.next()){
                result = r.getString("name");
            }
            }else{
                JOptionPane.showMessageDialog(null,"Invalid id!");
                return "";
            }
            return result;
        } catch (SQLException ex) {
            Logger.getLogger(BillFrame.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                c1.close();
                s.close();
                r.close();
            } catch (SQLException ex) {
                Logger.getLogger(BillFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return null;
    }
